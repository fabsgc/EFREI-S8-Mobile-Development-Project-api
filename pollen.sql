CREATE TABLE departments
(
    id INT(11) PRIMARY KEY NOT NULL AUTO_INCREMENT,
    name VARCHAR(32) NOT NULL,
    number VARCHAR(10) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE trees
(
    id INT(11) PRIMARY KEY NOT NULL AUTO_INCREMENT,
    name VARCHAR(32) NOT NULL,
    number INT(11) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE risks
(
    id INT(11) PRIMARY KEY NOT NULL AUTO_INCREMENT,
    risk FLOAT NOT NULL,
    department_id INT(11) NOT NULL,
    tree_id INT(11) NOT NULL,
    CONSTRAINT risks_department_id_fk FOREIGN KEY (department_id) REFERENCES departments (id),
    CONSTRAINT risks_tree_id_fk FOREIGN KEY (tree_id) REFERENCES trees (id)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE INDEX risks_departments_id_fk ON risks (department_id);
CREATE INDEX risks_trees_id_fk ON risks (tree_id);

